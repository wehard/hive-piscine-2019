int	ft_count_if(char **tab, int (*f)(char*))
{
	int c;
	int i;

	if (!tab || !tab[0])
		return (0);
	c = 0;
	i = 0;
	while (tab[i])
	{
		c += (*f)(tab[i]);
		i++;
	}
	return (c);
}
