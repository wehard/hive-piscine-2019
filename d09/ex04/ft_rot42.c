char	*ft_rot42(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i])
	{
		c = str[i];
		if (c >= 'a' && c <= 'z')
		{
			c = ((c - 'a' + 42) % 26) + 'a';
			str[i] = c;
		}
		else if (c >= 'A' && c <= 'Z')
		{
			c = ((c - 'A' + 42) % 26) + 'A';
			str[i] = c;
		}
		i++;
	}
	return (str);
}
