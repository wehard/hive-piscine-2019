int	ft_is_prime(int nb)
{
	int c;

	if (nb <= 1)
		return (0);
	if (nb == 4)
		return (0);
	c = 2;
	while (c < (nb / 2) + 1)
	{
		if (nb % c == 0)
		{
			return (0);
		}
		c++;
	}
	return (1);
}
